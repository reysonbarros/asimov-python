'''
Input: name and age
Output: Greeting message, name's length and age(5 years later)
'''
name = str(input("Type your name:"))
age = int(input("Type your age:"))
print(f"Hello {name} !!!")
print(f"Your name has {str(len(name))} characters.")
print(f"After 5 years you will be {str(age+5)} years old.")



